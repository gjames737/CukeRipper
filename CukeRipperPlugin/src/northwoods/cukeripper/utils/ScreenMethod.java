package northwoods.cukeripper.utils;

public class ScreenMethod {
	private String name;
	private String body;

	public ScreenMethod(String _name, String _body) {
		this.name = _name;
		this.body = _body;
	}

	public String getName() {
		return name;
	}

	public String getBody() {
		return body;
	}
}
